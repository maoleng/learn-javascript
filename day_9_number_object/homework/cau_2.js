// a - Đáy nhỏ
// b - Đáy lớn
// h - Chiều cao
// Hãy tính diện tích hình thang và lưu kết quả vào biến output.

// Ví dụ:

// Input: 20, 30, 60 - Output: 1500
// Input: 10, 30, 30 - Output: 600
// Input: 50, 30, 80 - Output: 3200


